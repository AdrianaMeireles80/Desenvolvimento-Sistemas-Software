    /**
     * Vai percorrer o hashmap de utilizadores e vai dar o print na tabela de todos eles
     * @param userDAO 
     */
    public void addRowToTable(UtilizadorDAO userDAO){
        
        DefaultTableModel model = (DefaultTableModel) tabelaFunc.getModel();
        
        Object rowData[] = new Object[4];
        
        for(String key : userDAO.keySet()){
            rowData[0] = userDAO.get(key).getNumero();
            rowData[1] = userDAO.get(key).getEmail_utilizador();
            rowData[2] = userDAO.get(key).getUsername();
            rowData[3] = userDAO.get(key).getPassword();
            model.addRow(rowData);
        }
    }
